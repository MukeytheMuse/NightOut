    @Override
    public void onLocationChanged(Location location) {
        //TextView tvLocation = (TextView) findViewById(R.id.tv_location);

        // Getting latitude of the current location
        double latitude = location.getLatitude();

        // Getting longitude of the current location
        double longitude = location.getLongitude();
        // Creating a LatLng object for the current location
        LatLng latLng = new LatLng(latitude, longitude);

        // Showing the current location in Google Map
        mMap.setOnMapClickListener(new GoogleMap.OnMapClickListener() {

            @Override
            public void onMapClick(LatLng point) {
                // TODO Auto-generated method stub

                id = UUID.randomUUID().toString();
                Geofence geofence = new Geofence.Builder()
                        .setRequestId(id)
                        .setTransitionTypes(Geofence.GEOFENCE_TRANSITION_ENTER)
                        .setCircularRegion(point.latitude, point.longitude, 1000)
                        .setExpirationDuration(Geofence.NEVER_EXPIRE)
                        .build();

                lstLatLngs.add(point);
                mMap.clear();
                mMap.addMarker(new MarkerOptions().position(point).title("GeoFence" + geofence.getRequestId())).showInfoWindow();

                CircleOptions circleOptions = new CircleOptions()
                        .center(new LatLng(point.latitude, point.longitude)).radius(1000)
                        .fillColor(Color.parseColor("#ce46df"));

                mMap.addCircle(circleOptions);
            }
        });

        mMap.moveCamera(CameraUpdateFactory.newLatLng(latLng));

        // Zoom in the Google Map
        mMap.animateCamera(CameraUpdateFactory.zoomTo(15));

        // Setting latitude and longitude in the TextView tv_location
        //tvLocation.setText("Latitude:" +  latitude  + ", Longitude:"+ longitude );
    }

